      implicit none
************************************************************************
*
*     Stores final BEAMPARAM results in common /BEAM/ and in bank
*
************************************************************************
      integer mcfil,mcnam,mcrng,mcstr,mcwrd,mreal,mwflt,mwnam,
     +mbbparam
